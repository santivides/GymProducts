    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtNombre.getText().length()==0){
            JOptionPane.showInternalConfirmDialog(rootPane, "ingrese el nombre del cliente");
            txtNombre.requestFocus();
        }
        if(txtApellidos.getText().length()==0){
            JOptionPane.showInternalConfirmDialog(rootPane, "ingrese el nombre del cliente");
            txtApellidos.requestFocus();
        }
        if(txtEdad.getText().length()==0){
            JOptionPane.showInternalConfirmDialog(rootPane, "ingrese el nombre del cliente");
               txtEdad.requestFocus();
        }
        if(txtDireccion.getText().length()==0){
            JOptionPane.showInternalConfirmDialog(rootPane, "ingrese el nombre del cliente");
            txtDireccion.requestFocus();
        }
        if(txtDni.getText().length()==0){
            JOptionPane.showInternalConfirmDialog(rootPane, "ingrese el nombre del cliente");
            txtDni.requestFocus();
        }
        
        Cliente dts = new Cliente();
        ConecCliente func = new ConecCliente();
        
        dts.setId(txtIdCliente.getText());
        dts.setNombre(txtNombre.getText());
        dts.setApellidos(txtApellidos.getText());
        dts.setDNI(txtDni.getText());
        dts.setRUC(txtRuc.getText());
        dts.setEdad(Integer.parseInt(txtEdad.getText()));
        int seleccionado = cbSexo.getSelectedIndex();
        dts.setSexo((String) cbSexo.getItemAt(seleccionado));
        dts.setTelefono(txtTelefono.getText());
        dts.setDireccion(txtDireccion.getText());
        
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "Cliente registrado");
                mostrar("");
                limpiar();
                inhabilitar();
            }
        }else if(accion.equals("editar")){
            if(func.insertar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "Informacion del cliente actualizada");
                mostrar("");
                limpiar();
                inhabilitar();
            }
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

